/*Note that any gl calls must always happen after a GL state is initialized */
static void initGeom() {

	//generate the VAO
	glGenVertexArrays(1, &VertexArrayID);
	glBindVertexArray(VertexArrayID);

	//generate vertex buffer to hand off to OGL
	glGenBuffers(1, &vertexbuffer);
	//set the current state to focus on our vertex buffer
	glBindBuffer(GL_ARRAY_BUFFER, vertexbuffer);
	//actually memcopy the data - only do this once
	glBufferData(GL_ARRAY_BUFFER, sizeof(g_primitive_buffer_data), g_primitive_buffer_data, GL_DYNAMIC_DRAW);
}

static float implicitEq(float x, float y, float z) {
   return pow(x - (LENGTH / 2), 2) + pow(y - (HEIGHT / 2), 2) + pow(z - (DEPTH / 2), 2) - pow(50, 2);
}

static void populateVolumeData() {
   for (int i = 0; i < LENGTH; i++) {
      for (int j = 0; j < HEIGHT; j++) {
         for (int k = 0; k < DEPTH; k++) {
            volume_data[IJK(i,j,k)] = implicitEq(i, j, k);
            //std::cout << "Coords: " << i << " " << j << " " << k << " " << volume_data[IJK(i,j,k)] << std::endl;
         }
      }
   }
}

static glm::vec3 midpoint(float i1, float j1, float k1, float i2, float j2, float k2) {
   glm::vec3 point;

   point.x = (i1 + i2) / 2;
   point.y = (j1 + j2) / 2;
   point.z = (k1 + k2) / 2;

   point.x = point.x - (LENGTH / 2);
   point.y = point.y - (HEIGHT / 2);
   point.z = point.z - (DEPTH / 2);

   return point;
}

// Return 
static void createTriangles(int i, int j, int k) {
   float isolevel = 0;
   glm::vec3 vertlist[12];

   // Front face
   float implicitVal0 = volume_data[IJK(i,j,k+1)];
   float implicitVal1 = volume_data[IJK(i+1,j,k+1)];
   float implicitVal2 = volume_data[IJK(i+1,j,k)];
   float implicitVal3 = volume_data[IJK(i,j,k)];
   // Back face
   float implicitVal4 = volume_data[IJK(i,j+1,k+1)];
   float implicitVal5 = volume_data[IJK(i+1,j+1,k+1)];
   float implicitVal6 = volume_data[IJK(i+1,j+1,k)];
   float implicitVal7 = volume_data[IJK(i,j+1,k)];

   int cubeindex = 0;
   if (implicitVal0 < isolevel) cubeindex |= 1;
   if (implicitVal1 < isolevel) cubeindex |= 2;
   if (implicitVal2 < isolevel) cubeindex |= 4;
   if (implicitVal3 < isolevel) cubeindex |= 8;
   if (implicitVal4 < isolevel) cubeindex |= 16;
   if (implicitVal5 < isolevel) cubeindex |= 32;
   if (implicitVal6 < isolevel) cubeindex |= 64;
   if (implicitVal7 < isolevel) cubeindex |= 128;

   /* Cube is entirely in/out of the surface */
   if (edgeTable[cubeindex] == 0)
      return;

   /* Find the vertices where the surface intersects the cube */
   if (edgeTable[cubeindex] & 1)
      vertlist[0] = midpoint(i,j,k+1,i+1,j,k+1);
   if (edgeTable[cubeindex] & 2)
      vertlist[1] = midpoint(i+1,j,k+1,i+1,j,k);
   if (edgeTable[cubeindex] & 4)
      vertlist[2] = midpoint(i+1,j,k,i,j,k);
   if (edgeTable[cubeindex] & 8)
      vertlist[3] = midpoint(i,j,k,i,j,k+1);
   if (edgeTable[cubeindex] & 16)
      vertlist[4] = midpoint(i,j+1,k+1,i+1,j+1,k+1);
   if (edgeTable[cubeindex] & 32)
      vertlist[5] = midpoint(i+1,j+1,k+1,i+1,j+1,k);
   if (edgeTable[cubeindex] & 64)
      vertlist[6] = midpoint(i+1,j+1,k,i,j+1,k);
   if (edgeTable[cubeindex] & 128)
      vertlist[7] = midpoint(i,j+1,k,i,j+1,k+1);
   if (edgeTable[cubeindex] & 256)
      vertlist[8] = midpoint(i,j,k+1,i,j+1,k+1);
   if (edgeTable[cubeindex] & 512)
      vertlist[9] = midpoint(i+1,j,k+1,i+1,j+1,k+1);
   if (edgeTable[cubeindex] & 1024)
      vertlist[10] = midpoint(i+1,j,k,i+1,j+1,k);
   if (edgeTable[cubeindex] & 2048)
      vertlist[11] = midpoint(i,j,k,i,j+1,k);

   for (int i=0; triTable[cubeindex][i] != -1; i+=3) {
      triangles.push_back(vertlist[triTable[cubeindex][i]].x / LENGTH);
      triangles.push_back(vertlist[triTable[cubeindex][i]].y / HEIGHT);
      triangles.push_back(vertlist[triTable[cubeindex][i]].z / DEPTH);

      triangles.push_back(vertlist[triTable[cubeindex][i+1]].x / LENGTH);
      triangles.push_back(vertlist[triTable[cubeindex][i+1]].y / HEIGHT);
      triangles.push_back(vertlist[triTable[cubeindex][i+1]].z / DEPTH);

      triangles.push_back(vertlist[triTable[cubeindex][i+2]].x / LENGTH);
      triangles.push_back(vertlist[triTable[cubeindex][i+2]].y / HEIGHT);
      triangles.push_back(vertlist[triTable[cubeindex][i+2]].z / DEPTH);
   }
}

static void drawVolumeData() {
   for (int i = 0; i < LENGTH - 1; i++) {
      for (int j = 0; j < HEIGHT - 1; j++) {
         for (int k = 0; k < DEPTH - 1; k++) {
            createTriangles(i, j, k);
         }
      }
   }
}

//General OGL initialization - set OGL state here
static void init()
{
	GLSL::checkVersion();

	// Set background color.
	glClearColor(1.0f, 1.0f, 1.0f, 1.0f);
	// Enable z-buffer test.
	glEnable(GL_DEPTH_TEST);

	// Initialize the GLSL program.
	prog = make_shared<Program>();
	prog->setVerbose(true);
	prog->setShaderNames(RESOURCE_DIR + "march_vert.glsl", RESOURCE_DIR + "march_frag.glsl");
	prog->init();
	prog->addUniform("P");
	prog->addUniform("MV");
	prog->addAttribute("vertPos");

   populateVolumeData();
   drawVolumeData();
   memcpy(g_primitive_buffer_data, triangles.data(), triangles.size() * sizeof(GLfloat));
}

static void render()
{
	// Get current frame buffer size.
	int width, height;
	glfwGetFramebufferSize(window, &width, &height);
	float aspect = width/(float)height;
	glViewport(0, 0, width, height);

	// Clear framebuffer.
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	// Create the matrix stacks - please leave these alone for now
	auto P = make_shared<MatrixStack>();
	auto MV = make_shared<MatrixStack>();
	// Apply orthographic projection.
	P->pushMatrix();
	if (width > height) {
		P->ortho(-2*aspect, 2*aspect, -2, 2, -2, 100.0f);
	} else {
		P->ortho(-2, 2, -2*1/aspect, 2*1/aspect, -2, 100.0f);
	}
	MV->pushMatrix();

	// Draw the triangle using GLSL.
	prog->bind();

	//send the matrices to the shaders
	glUniformMatrix4fv(prog->getUniform("P"), 1, GL_FALSE, value_ptr(P->topMatrix()));
	glUniformMatrix4fv(prog->getUniform("MV"), 1, GL_FALSE, value_ptr(MV->topMatrix()));

	//we need to set up the vertex array
	glEnableVertexAttribArray(0);
	glBindBuffer(GL_ARRAY_BUFFER, vertexbuffer);
	//key function to get up how many elements to pull out at a time (3)
	glBufferData(GL_ARRAY_BUFFER, sizeof(g_primitive_buffer_data), g_primitive_buffer_data, GL_DYNAMIC_DRAW);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 0, (void*) 0);

	//actually draw from vertex 0, 3 vertices
	glDrawArrays(GL_TRIANGLES, 0, triangles.size());
	glDisableVertexAttribArray(0);

	prog->unbind();

	// Pop matrix stacks.
	MV->popMatrix();
	P->popMatrix();
}

